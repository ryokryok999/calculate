package calculate;

public class Calc {

	public int add(int augent, int addend) {
		return augent + addend;
	}

	public int sub(int minuend, int subtrahend) {
		return minuend - subtrahend;
	}

	public int mul(int multiplicand, int pultiplier) {
		return multiplicand * pultiplier;
	}

	public int div(int dividend, int divisor) {
		return dividend / divisor;
	}


}
